package edu.swe266.Object;

public class Account {
    private int accid;
    private int balance;
    private int psw;

    public void setAccid(int accid) {
        this.accid = accid;
    }

    public void setBalance(int balance) {
        this.balance = balance;
    }

    public int getAccid() {
        return accid;
    }

    public int getBalance() {
        return balance;
    }

    public void setPsw(int psw) {
        this.psw = psw;
    }

    public int getPsw() {
        return psw;
    }
}
